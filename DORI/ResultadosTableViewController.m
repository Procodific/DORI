- (void) viewWillAppear:(BOOL)animated {
    _minitests = [_paciente.minitests array];
    
    self.title = [ [NSString alloc] initWithFormat:@"Tests de %@", _paciente.nombre];
    [self.tableView reloadData];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return [_minitests count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    NSString *cellIdentifier = @"Cell";
    UITableViewCell *cell = (UITableViewCell *) [tableView dequeueReusableCellWithIdentifier:cellIdentifier forIndexPath:indexPath];
    
    NSString *texto = [[NSString alloc] initWithFormat:@"Test %@. Puntaje: %@", [_minitests[indexPath.row] fecha], [_minitests[indexPath.row] puntaje]];
    cell.textLabel.text = texto;
    
    return cell;
}

